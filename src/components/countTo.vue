<template>
    {{ d.num.toFixed(0) }}
</template>

<script setup>
import { reactive, watch } from 'vue';
import gsap from 'gsap';

const props = defineProps({
    value: {
        type: Number,
        default: 0
    }
})

const d = reactive({
    num: 0
})

// 数字上涨动画
function animateToValue() {
    gsap.to(d, {
        duration: 0.3,
        num: props.value
    })
}

animateToValue()

watch(() => props.value, () => animateToValue())
</script>